# automations/
# ─────────────────────────────────────────────────────────────────────────────
# Módulos de automação organizados por domínio.
#
# Cada subpasta é um domínio independente. Para adicionar um novo domínio:
#   1. Crie a pasta: automations/<dominio>/
#   2. Crie o __init__.py com a descrição do domínio
#   3. Adicione os módulos .py dentro
#   4. Documente no README.md raiz
#
# Domínios ativos:
#   network/   → automações de rede e recon
#
# Domínios planejados:
#   agents/    → gerenciamento e monitoramento de agentes
#   reports/   → geração e exportação de relatórios
#   payloads/  → automação de geração de payloads
# ─────────────────────────────────────────────────────────────────────────────
